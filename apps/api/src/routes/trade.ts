// ─── ROUTE PLUGIN ────────────────────────────────────────────────────────────

export const registerTradeRoutes: FastifyPluginAsync = async (app) => {

  app.addHook('onRoute', (routeOptions) => {
    routeOptions.schema ??= {}
    routeOptions.schema.tags = routeOptions.schema.tags ?? ['trade']
  })

  // ─── GET /commodity-flows ─────────────────────────────────
  app.get('/commodity-flows', {
    schema: {
      summary: 'Strategic Commodity Flows',
      description: 'UN Comtrade trade flow intelligence for strategic commodities (oil, uranium, chips, wheat, arms)',
      querystring: {
        type: 'object',
        properties: {
          cmdCode:  { type: 'string',  description: 'HS commodity code filter (4 or 6-digit)' },
          reporter: { type: 'integer', description: 'M49 reporter country code filter'         },
          limit:    { type: 'integer', default: 50, minimum: 1, maximum: 500                   },
        },
      },
    },
    config: { rateLimit: { max: 30, timeWindow: '1 minute' } },
  }, async (req, reply) => {
    const { cmdCode: rawCmdCode, reporter, limit = 50 } = req.query as {
      cmdCode?: string
      reporter?: number
      limit?: number
    }

    // Normalise 6-digit → 4-digit HS code
    const cmdCode = rawCmdCode ? (UI_HS_ALIAS[rawCmdCode] ?? rawCmdCode) : undefined

    const cacheKey = `trade:commodity-flows:${cmdCode ?? 'all'}:${reporter ?? 'all'}:${limit}`
    const cached = await redis.get(cacheKey).catch(() => null)
    if (cached) {
      return reply.header('X-Cache-Hit', 'true').send(JSON.parse(cached))
    }

    let query = db('signals')
      .where('category', 'economy')
      .whereRaw("tags @> ARRAY['comtrade']::text[]")
      .orderBy('created_at', 'desc')
      .limit(limit * 5)  // over-fetch to allow post-filter
      .select('id', 'title', 'summary', 'tags', 'created_at', 'severity')

    if (cmdCode) {
      query = query.whereRaw('tags @> ARRAY[?]::text[]', [cmdCode])
    }

    const rows = await query as SignalRow[]

    const flows: ComtradeFlow[] = []
    const commoditySet = new Set<string>()
    let lastUpdated = ''

    for (const row of rows) {
      const flow = signalToFlow(row)
      if (!flow) continue
      if (reporter != null && flow.reporterCode !== reporter) continue
      flows.push(flow)
      if (flow.cmdCode) commoditySet.add(flow.cmdCode)
      if (!lastUpdated || row.created_at > lastUpdated) lastUpdated = row.created_at
    }

    // If no live data yet, use seed data with real UN Comtrade figures
    if (flows.length === 0) {
      let seeded = SEED_FLOWS
      if (cmdCode) seeded = seeded.filter(f => f.cmdCode === cmdCode)
      const response = {
        flows:       seeded.slice(0, limit),
        commodities: [...new Set(seeded.map(f => f.cmdCode))],
        lastUpdated: new Date().toISOString(),
        source:      'seed',
      }
      return reply.send(response)
    }

    flows.sort((a, b) => b.primaryValue - a.primaryValue)

    const response = {
      flows:       flows.slice(0, limit),
      commodities: Array.from(commoditySet),
      lastUpdated: lastUpdated || new Date().toISOString(),
    }

    await redis.setex(cacheKey, ROUTE_CACHE_TTL, JSON.stringify(response)).catch(() => {})
    return reply.send(response)
  })

  // ─── GET /commodity-flows/:cmdCode ───────────────────────
  app.get<{ Params: { cmdCode: string }; Querystring: { reporter?: number; limit?: number } }>(
    '/commodity-flows/:cmdCode',
    {
      schema: {
        summary: 'Commodity Flows by HS Code',
        description: 'UN Comtrade flow intelligence for a specific HS commodity code',
        params: {
          type: 'object',
          properties: {
            cmdCode: { type: 'string', description: 'HS commodity code (4 or 6-digit)' },
          },
          required: ['cmdCode'],
        },
        querystring: {
          type: 'object',
          properties: {
            reporter: { type: 'integer', description: 'M49 reporter country code filter' },
            limit:    { type: 'integer', default: 50, minimum: 1, maximum: 500 },
          },
        },
      },
      config: { rateLimit: { max: 30, timeWindow: '1 minute' } },
    },
    async (req, reply) => {
      const rawCmdCode = req.params.cmdCode
      const { reporter, limit = 50 } = req.query

      // Normalise 6-digit → 4-digit HS code
      const cmdCode = UI_HS_ALIAS[rawCmdCode] ?? rawCmdCode

      const cacheKey = `trade:commodity-flows:${cmdCode}:${reporter ?? 'all'}:${limit}`
      const cached = await redis.get(cacheKey).catch(() => null)
      if (cached) {
        return reply.header('X-Cache-Hit', 'true').send(JSON.parse(cached))
      }

      const rows = await db('signals')
        .where('category', 'economy')
        .whereRaw("tags @> ARRAY['comtrade']::text[]")
        .whereRaw('tags @> ARRAY[?]::text[]', [cmdCode])
        .orderBy('created_at', 'desc')
        .limit(limit * 5)
        .select('id', 'title', 'summary', 'tags', 'created_at', 'severity') as SignalRow[]

      let flows: ComtradeFlow[] = []
      let lastUpdated = ''

      for (const row of rows) {
        const flow = signalToFlow(row)
        if (!flow) continue
        if (reporter != null && flow.reporterCode !== reporter) continue
        flows.push(flow)
        if (!lastUpdated || row.created_at > lastUpdated) lastUpdated = row.created_at
      }

      flows.sort((a, b) => b.primaryValue - a.primaryValue)
      flows = flows.slice(0, limit)

      const response = {
        flows,
        commodities: [cmdCode],
        commodity:   HS_COMMODITY_NAMES[cmdCode] ?? cmdCode,
        lastUpdated: lastUpdated || new Date().toISOString(),
      }

      await redis.setex(cacheKey, ROUTE_CACHE_TTL, JSON.stringify(response)).catch(() => {})
      return reply.send(response)
    },
  )
}
