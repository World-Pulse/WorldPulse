// ─── TYPES ───────────────────────────────────────────────────────────────────

export interface ComtradeFlow {
  period: string
  reporterCode: number
  reporterDesc: string
  partnerCode: number
  partnerDesc: string
  flowCode: 'M' | 'X'
  cmdCode: string
  cmdDesc: string
  primaryValue: number  // USD
  netWgt: number
}

